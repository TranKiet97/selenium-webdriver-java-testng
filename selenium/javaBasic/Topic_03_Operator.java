package javaBasic;

public class Topic_03_Operator {

	public static void main(String[] args) {
		int number = 10;
		number +=5;
		System.out.println(number);
		System.out.println(number++);	// Thực hiện hành động trước sau đó mới tăng 1
		System.out.println(++number);	// Tăng 1 trước sau đó thực hiện hành động 
		System.out.println(number--);
	}

}
